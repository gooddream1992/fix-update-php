
<h2>Your privacy is important to us.</h2>

<p>We want you to feel comfortable using SigFly services. Because
we gather certain types of information about the users of SigFly services, we feel you should fully
understand what information we gather and how we use that information.</p>

<h2>What information does SigFly gather?</h2>

<p>When you register for an affiliate account on this website, SigFly obtains contact information,
such as your name, address, and phone number. When you use SigFly services, our systems
record information about your session, such as the dates and times of use, and the specific
Internet address assigned to you. SigFly also tracks requests you make for information on
SigFly products and services. Finally, SigFly keeps records of your payment history and
communications you have with SigFly support team.</p>

<h2>What does SigFly do with this information?</h2>

<p>SigFly uses the information it gathers in order to provide you with the best online experience
possible. As you might expect, contact information is used to contact you concerning your
account and our company. Payment information is used to validate your identity and to pay you
for the commissions earned. Information on your communications with SigFly support team is
used to address the issues raised by your communications. In addition, SigFly regularly sends
out mailings regarding the operation of its service and other goods and services that it believes
may be of interest to you. You may request that SigFly not send you mailings relating to other
goods and services at the URLs listed in this section or send an email to privacy@SigFly.com.
SigFly reserves the right, however, to send bulletins and other important information regarding
the service at its discretion.</p>

<h2>Does SigFly share affiliate information with third parties?</h2>

<p>SigFly will not share or disclose information regarding its affiliates except (i) when we receive
legal process such as a court order; (ii) when reasonably necessary to protect the company's
rights or property; or (iii) in emergencies where someone's safety is at risk.
ii.You should be aware, however, that any information that you disclose in a public space such
as a chat room, bulletin board, or personal Web site, SigFly DNR domain registration is freely
available to anyone else visiting that space. SigFly cannot safeguard any personally identifiable
information you disclose in these locations, nor can it ensure that information you voluntarily
supply to third parties remains private. Please review the privacy policies of third party sites you
frequent to ensure that your privacy preferences are respected.</p>

<h2>How can I correct or update the information?</h2>

<p>To request a copy of the contact information SigFly has on your account, send an email request
to privacy@SigFly.com. You may correct that information as appropriate by returning a message
to the same address. SigFly may require a copy of a government issued form of identification
before disclosing information or before making changes to the contact information on an account.</p>

<h2>What happens to the information if I leave SigFly?</h2>

<p>If you ever leave SigFly, SigFly will normally delete your personally identifiable account
information twelve months after the account is cancelled, unless we are under a legal obligation
to retain it. We preserve account information for this short period of time in the event you wish
to reactivate your account and retain your previous e-mail address. However, in the event of
any accounting irregularities, SigFly reserves the right to retain your contact and payment
information as necessary to resolve the matter.</p>

<h2>Changes to this policy and questions about privacy.</h2>

<p>SigFly reserves the right to make changes to this policy. Those changes will become effective
30 days after notice of them is posted here. If you have any questions about this privacy policy,
please send them via email to: privacy@SigFly.com.</p>

